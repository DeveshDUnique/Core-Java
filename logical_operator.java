public class logical_operator {
    public static void main(String[] args)
    {
        // two or more comparisons
        // And,  or , not

        int a =9;
        int b =5;
        int x =7;
        int y =3;

        boolean result = x>y && a<b; // false
        System.out.println(result);
        boolean result1 = x>y || a<b;  // true
        System.out.println(result1);


    }
    
}
